public class array5_1 {
    public static void main(String[] args){

        // 練習問題5-1
        // 次のプログラムを作成しなさい。
        // 10 個の数値を入力する。
        // 入力された各々の数値を 2 倍にして表示する。

        for(int i = 1; i < 11; i++){
            int num = i * 2;
            System.out.print(i + " * 2 = " + num + "\n");
        }
        System.out.println();

    }
}
